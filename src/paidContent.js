function pairKey(firstElement, secondElement) {
  const direct = `${firstElement}-${secondElement}`;
  if (compatibilityTemplates[direct]) return direct;

  const reverse = `${secondElement}-${firstElement}`;
  if (compatibilityTemplates[reverse]) return reverse;

  return direct;
}

function communicationFocus(element) {
  const focus = {
    fire: 'прямой отклик, честность и ощущение движения',
    earth: 'понятную форму, надёжность и уважение к темпу',
    air: 'слова, пространство для мысли и возможность объяснить',
    water: 'эмоциональную включённость, безопасность и мягкий тон',
    unknown: 'ясность, уважение и спокойный темп'
  };

  return focus[element] || focus.unknown;
}

function formatMaybeDate(isoDate) {
  if (!isoDate) return 'не указана';
  const [year, month, day] = String(isoDate).split('-');
  if (!year || !month || !day) return isoDate;
  return `${day}.${month}.${year}`;
}

function capitalize(text) {
  return String(text || '').replace(/^./u, (char) => char.toUpperCase());
}

function CapitalizeSentence(text) {
  return capitalize(text);
}
