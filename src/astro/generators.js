function pairKey(firstElement, secondElement) {
  const direct = `${firstElement}-${secondElement}`;
  if (ELEMENT_PAIRS[direct]) return direct;

  const reverse = `${secondElement}-${firstElement}`;
  if (ELEMENT_PAIRS[reverse]) return reverse;

  return direct;
}

function shorten(text) {
  const sentences = text.split(/(?<=[.!?])\s+/).slice(0, 2).join(' ');
  return sentences || text;
}
