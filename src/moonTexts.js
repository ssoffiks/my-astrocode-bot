export function moonResultText(result) {
  const interpretation = MOON_INTERPRETATIONS[result.sign] || MOON_INTERPRETATIONS['Рыбы'];
  const approximateNote = result.timeUnknown
    ? `Так как время рождения неизвестно, расчёт примерный. Для точного результата лучше знать хотя бы примерное время рождения.\n\n`
    : '';
  const transitionNote = result.dayRange?.changedSign
    ? `В этот день Луна могла перейти из одного знака в другой, поэтому без точного времени рождения результат может быть неточным.\n\n`
    : '';

  return `🌙 Твоя Луна: Луна в ${moonSignInCase(result.sign)}

${approximateNote}${transitionNote}Луна отвечает за эмоциональные реакции, внутреннюю безопасность, привычный способ успокаиваться и то, как ты проживаешь близость.

${interpretation.text}

Что может помогать:
— ${interpretation.helps[0]}
— ${interpretation.helps[1]}
— ${interpretation.helps[2]}

Это не “приговор”, а мягкая подсказка для самонаблюдения.`;
}

